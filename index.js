function Flash() {
    light.setBrightness(50)
    light.setPixelColor(2, 0xff8000)
    control.waitMicros(25000)
    light.setPixelColor(3, 0xff8000)
    light.setPixelColor(2, 0x0000ff)
    light.setPixelColor(1, 0xff8000)
    control.waitMicros(25000)
    light.setPixelColor(2, 0)
    light.setPixelColor(3, 0x0000ff)
    light.setPixelColor(1, 0x0000ff)
    light.setPixelColor(4, 0xff8000)
    light.setPixelColor(0, 0xff8000)
    control.waitMicros(25000)
    light.setPixelColor(3, 0)
    light.setPixelColor(1, 0)
    light.setPixelColor(4, 0x0000ff)
    light.setPixelColor(0, 0)
    control.waitMicros(25000)
    light.setBrightness(0)
}
